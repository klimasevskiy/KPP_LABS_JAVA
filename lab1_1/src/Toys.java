public class Toys {
    public String Name;
    public int Price;
    public int Age_From;
    public int Age_To;
    public String Characteristic;


    public String getName() {
        return Name;
    }

    public void setName(String newName) {
        this.Name = newName;
    }

    public int getPrice() {
        return Price;
    }

    public void setPrice(int newPrice) {
        this.Price = newPrice;
    }

    public int getAge_From() {
        return Age_From;
    }

    public void setAge_From(int newAge_From) {
        this.Age_From = newAge_From;
    }

    public int getAge_To() {
        return Age_To;
    }

    public void setAge_To(int newAge_To) {
        this.Age_To = newAge_To;
    }

    public String getCharacteristic() {
        return Characteristic;
    }

    public void setCharacteristic(String newCharacteristic) {
        this.Characteristic = newCharacteristic;
    }

}